def normalize_path_key(s: str) -> str:
    """统一路径中的 Unicode 形式与空白，避免隐藏空格/全角空格导致匹配失败"""
    s = unicodedata.normalize("NFKC", s)
    s = s.replace("\u3000", " ")
    s = s.replace("\xa0", " ")
    s = s.replace("\u200b", "")
    s = re.sub(r"[ \t]+", " ", s)
    return s.strip()

def backup_file(file_path: Path) -> Path:
    """备份文件"""
    if not file_path.exists():
        return None
    BACKUP_DIR.mkdir(parents=True, exist_ok=True)
    relative_path = file_path.relative_to(BASE_DIR)
    backup_path = BACKUP_DIR / relative_path
    backup_path.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(file_path, backup_path)
    return backup_path

def scan_files():
    """扫描当前本地实际文件，建立文件路径映射表（使用规范化 key）"""
    file_map = {}
    raw_file_map = {}
    for md_file in BASE_DIR.glob("**/*.md"):
        rel_path = str(md_file.relative_to(BASE_DIR))
        if rel_path.startswith('backups/'):
            continue
        if '__MACOSX' in rel_path:
            continue
        if '/._' in rel_path or md_file.name.startswith('._'):
            continue
        if '.DS_Store' in rel_path or md_file.name == '.DS_Store':
            continue
        norm_rel_path = normalize_path_key(rel_path)
        file_map[norm_rel_path] = md_file
        raw_file_map[norm_rel_path] = rel_path
    return file_map, raw_file_map

